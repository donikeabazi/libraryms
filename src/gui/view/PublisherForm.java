    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            int row = table.getSelectedRow();
            if (row == -1) {
                Publisher p = new Publisher();
                p.setName(emriTxt.getText());
                p.setCountry(countryTxt.getText());
                pr.create(p);
            } else {
                Publisher p = ptm.getPublisher(row);
                p.setName(emriTxt.getText());
                p.setCountry(countryTxt.getText());
                pr.edit(p);
            }
            clear();
            loadTable();
        } catch (LibraryException ex) {
            JOptionPane.showMessageDialog(this, "E dhena ekziton!");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

    private void delBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBtnActionPerformed
        int row = table.getSelectedRow();
        if (row != -1) {
            Object[] ob = {"Po", "Jo"};
            int i = JOptionPane.showOptionDialog(this, "A dÃ«shironi ta fshini ?", "Fshirja", 
			JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE, null, ob, ob[1]);
            if (i == 0) {
                Publisher p = ptm.getPublisher(row);
                try {
                    pr.delete(p);
                } catch (LibraryException ex) {
                    JOptionPane.showMessageDialog(this,ex.getMessage());
                }
                clear();
                loadTable();
            } else {
                clear();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Nuk keni selektuar asgje per te fshire!");
        }
    }//GEN-LAST:event_delBtnActionPerformed

