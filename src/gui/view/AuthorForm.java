    private void tableSelectedIndexChange(){
        final ListSelectionModel rowSM = table.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener(){
            public void valueChanged(ListSelectionEvent e){
               if(e.getValueIsAdjusting()){
                   return;
               } 
               ListSelectionModel rowSM = (ListSelectionModel) e.getSource();
               int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    Author a = atm.getAuthor(selectedIndex);
                    idTxt.setText(a.getId().toString());
                    emriTxt.setText(a.getFirstName());
                    mbiemriTxt.setText(a.getLastName());
                    dateOfBirth.setDate(a.getDateOfBirth());
                    cityTxt.setText(a.getCity());
                    countryTxt.setText(a.getCountry());                    
                }
            }
        });
    }
    
    public void loadTable(){
        try{
            List<Author> lista = ar.findAll();
            atm.add(lista);
            table.setModel(atm);
            atm.fireTableDataChanged();
        }catch(LibraryException ex){
            Logger.getLogger(AuthorForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
