    private void tableSelectedIndexChange(){
        final ListSelectionModel rowSM = table.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener(){
            public void valueChanged(ListSelectionEvent e){
               if(e.getValueIsAdjusting()){
                   return;
               } 
               ListSelectionModel rowSM = (ListSelectionModel) e.getSource();
               int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    Book b = btm.getBook(selectedIndex);
                    idTxt.setText(b.getId().toString());
                    titleTxt.setText(b.getTitle());
                    descTxt.setText(b.getDescription());
                    isbnTxt.setText(b.getIsbn());                   
                }
            }
        });
    }
    
    public void loadTable(){
        try{
            List<Book> lista = br.findAll();
            btm.add(lista);
            table.setModel(btm);
            btm.fireTableDataChanged();
        }catch(LibraryException ex){
            Logger.getLogger(AuthorForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void loadComboBox() {
        try {
            List<Author> lista = ar.findAll();
            acbm.add(lista);
            authorComboBox.setModel(acbm);
            authorComboBox.repaint();
            
            
            List<Publisher> publishers = pr.findAll();
            pcbm.add(publishers);
            publisherCB.setModel(pcbm);
            publisherCB.repaint();
        } catch (LibraryException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }

