    private void tableSelectedIndexChange(){
        final ListSelectionModel rowSM = table.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener(){
            public void valueChanged(ListSelectionEvent e){
               if(e.getValueIsAdjusting()){
                   return;
               } 
               ListSelectionModel rowSM = (ListSelectionModel) e.getSource();
               int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    Publisher p = ptm.getPublisher(selectedIndex);
                    idTxt.setText(p.getId().toString());
                    emriTxt.setText(p.getName());
                    countryTxt.setText(p.getCountry());                    
                }
            }
        });
    }
    
    public void loadTable(){
        try{
            List<Publisher> lista = pr.findAll();
            ptm.add(lista);
            table.setModel(ptm);
            ptm.fireTableDataChanged();
        }catch(LibraryException ex){
            Logger.getLogger(AuthorForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

