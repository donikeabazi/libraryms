    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            int row = table.getSelectedRow();
            if (row == -1) {
                Author a = new Author();
                a.setFirstName(emriTxt.getText());
                a.setLastName(mbiemriTxt.getText());
                a.setDateOfBirth(dateOfBirth.getDate());
                a.setCity(cityTxt.getText());
                a.setCountry(countryTxt.getText());
                ar.create(a);
            } else {
                Author a = atm.getAuthor(row);
                a.setFirstName(emriTxt.getText());
                a.setLastName(mbiemriTxt.getText());
                a.setDateOfBirth(dateOfBirth.getDate());
                a.setCity(cityTxt.getText());
                a.setCountry(countryTxt.getText());
                ar.edit(a);
            }
            clear();
            loadTable();
        } catch (LibraryException ex) {
            JOptionPane.showMessageDialog(this, "E dhena ekziton!");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

    private void delBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBtnActionPerformed
        int row = table.getSelectedRow();
        if (row != -1) {
            Object[] ob = {"Po", "Jo"};
            int i = JOptionPane.showOptionDialog(this, "A dÃ«shironi ta fshini ?", "Fshirja", 
			JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE, null, ob, ob[1]);
            if (i == 0) {
                Author a = atm.getAuthor(row);
                try {
                    ar.delete(a);
                } catch (LibraryException ex) {
                    JOptionPane.showMessageDialog(this,ex.getMessage());
                }
                clear();
                loadTable();
            } else {
                clear();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Nuk keni selektuar asgje per te fshire!");
        }
    }//GEN-LAST:event_delBtnActionPerformed

